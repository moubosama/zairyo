/**
 * 造作材の材積換算層（下地材の拾い量 → 発注材積 m³）
 *
 * プロの拾い出しXLS「造作材集計」シートの数式を踏襲する:
 *   材積(m³) = 断面H(mm) × 断面D(mm) × 長さL(mm) × 本数 × 10⁻⁹
 * ここでは「本数×1本長さ」を合計材長(m)にまとめて受け取る。
 *
 * 【XLS解読メモ（2026-07-15・②木及び建材XLSの数式を直接確認）】
 * - タイプ別シートの「間仕切下地(木)」行は 幅×高さ の積和−開口（=壁面積㎡）で拾われており、
 *   集計表の 84.082「m」は単位表記に反して実態は「壁1枚あたり片面の下地面積(㎡)」。
 *   高さは天井高ではなく 2.57 / 2.77 = 天井高(2200/2400) + 370mm（床仕上げ面〜上階スラブ下端。
 *   G展開図の全高2810から: 居室 2810−直貼40−2400=370 / 水回り 2810−置床200−仕上40−2200=370）。
 * - 一方、見積明細（発注の正）の間仕切木軸は 77.3m³/67戸 = 約855m/戸で、
 *   84㎡×断面(0.11m³)の約10倍 → 実際の軸組（両面×縦横@450）の材長に相当する。
 *   この層で「拾い面積 → 実発注材長 → 材積」の換算を行う。
 */

/** 断面規格（見積明細・木材ブロックの表記に対応） */
export const TIMBER_SECTIONS = {
  kiwaneta: { h: 45, d: 30, name: '際根太', spec: 'LVL 30×45' },
  majikiri: { h: 45, d: 30, name: '間仕切木軸', spec: 'LVL 30×45' },
  dobuchi: { h: 45, d: 30, name: '木胴縁（界壁面）', spec: 'LVL 30×45' },
  ceiling: { h: 40, d: 30, name: '天井下地', spec: 'LVL 30×40' },
};

/** 材積(m³) = H(mm)×D(mm)×合計材長(m→mm)×10⁻⁹（XLS造作材集計と同じ丸め=小数4桁） */
export function timberVolumeM3(section, lengthM) {
  if (!section || !(lengthM > 0)) return 0;
  return Math.round(section.h * section.d * (lengthM * 1000) * 1e-9 * 10000) / 10000;
}

/**
 * 間仕切下地(木)の拾い量（壁1枚あたり片面の下地面積㎡。XLS慣行で単位は"m"）→ 実発注材長(m)
 *
 * 幾何的根拠: 木軸間仕切は成30mmの材を壁の面ごとに組む（1本で両面のボードは受けられない）ため、
 *   縦桟（スタッド）@450 両面: 2 × 1/0.45 = 4.44 m/㎡
 *   横桟（胴縁・上下ランナー含む）@450 両面: 2 × 1/0.45 = 4.44 m/㎡
 *   計 8.89 m/㎡
 * 検算: Gタイプ拾い量84.082 × 8.89 × 断面0.00135㎡ = 1.01 m³/戸
 *       ≒ 見積明細 間仕切木軸 77.3m³/67戸 = 1.15 m³/戸（−12%）
 */
export const MAJIKIRI_TIMBER_M_PER_SQM = 2 * (1 / 0.45) + 2 * (1 / 0.45);

export function majikiriTimberLengthM(shitajiSqm) {
  return shitajiSqm > 0 ? shitajiSqm * MAJIKIRI_TIMBER_M_PER_SQM : 0;
}

/**
 * 天井下地の材長(m) = 天井面積(㎡) × (野縁@303格子 + 吊木)
 *
 * 幾何的根拠: 野縁30×40を@303で縦横の格子に組む（2/0.303 = 6.6 m/㎡）+
 *   吊木@910角（1/0.91² ≒ 1.2本/㎡）× 天井ふところ370mm（上記XLS解読メモの下地高と同値）。
 * 検算: G天井59.09㎡ → 417m → 0.50 m³ ≒ 見積明細 天井下地 38.5m³/67戸 = 0.57 m³/戸（−13%）
 * （@303片方向+野縁受け@910だと4.4m/㎡=0.31m³となり実績の半分 → 格子組みが実績と整合）
 */
export const CEILING_FRAME_M_PER_SQM = 2 / 0.303 + (1 / (0.91 * 0.91)) * 0.37;

export function ceilingFrameLengthM(ceilingSqm) {
  return ceilingSqm > 0 ? ceilingSqm * CEILING_FRAME_M_PER_SQM : 0;
}

/**
 * 木胴縁の材長(m) = RC面木（D下地）・界壁面などの胴縁下地面積(㎡) × 横胴縁@455
 * 幾何的根拠: RC面のボード受けは横胴縁@455（1/0.455 ≒ 2.2 m/㎡）。
 * ※ 67戸平均 3.6m³/67戸=0.054m³/戸 は界壁面胴縁を含む全タイプ平均。
 *   Gタイプは界壁がC04（打放クロス直・胴縁なし）のため平均より少なくて正しい。
 */
export const DOBUCHI_M_PER_SQM = 1 / 0.455;

export function dobuchiLengthM(rcFaceSqm) {
  return rcFaceSqm > 0 ? rcFaceSqm * DOBUCHI_M_PER_SQM : 0;
}
